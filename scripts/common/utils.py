import pandas as pd
from pathlib import Path
import sys

# 将项目根目录加入 Python 路径，以便导入 config
PROJECT_ROOT = Path(__file__).parent.parent.parent
sys.path.insert(0, str(PROJECT_ROOT))
import config


def get_us_trading_calendar(start: str = None, end: str = None) -> pd.DatetimeIndex:
    """
    获取美股交易日历（NYSE）
    使用 pandas 的 date_range 生成工作日，并剔除主要假期。
    更精确的方法可以用 pandas_market_calendars，这里提供简易版本。
    """
    if start is None:
        start = config.START_DATE
    if end is None:
        end = config.END_DATE

    # 生成所有工作日（周一到周五）
    trading_days = pd.date_range(start=start, end=end, freq='B')

    # 定义美股主要假期（示例：2018-2024，实际可扩展或从外部文件加载）
    us_holidays = [
        # 新年
        '2018-01-01', '2019-01-01', '2020-01-01', '2021-01-01', '2022-01-01', '2023-01-01', '2024-01-01',
        # 马丁·路德·金纪念日（1月第三个周一）
        '2018-01-15', '2019-01-21', '2020-01-20', '2021-01-18', '2022-01-17', '2023-01-16', '2024-01-15',
        # 总统日（2月第三个周一）
        '2018-02-19', '2019-02-18', '2020-02-17', '2021-02-15', '2022-02-21', '2023-02-20', '2024-02-19',
        # 耶稣受难日（不固定，这里简化，可略）
        # 阵亡将士纪念日（5月最后一个周一）
        '2018-05-28', '2019-05-27', '2020-05-25', '2021-05-31', '2022-05-30', '2023-05-29', '2024-05-27',
        # 六月节（2021年起）
        '2021-06-18', '2022-06-20', '2023-06-19', '2024-06-19',
        # 独立日
        '2018-07-04', '2019-07-04', '2020-07-03', '2021-07-05', '2022-07-04', '2023-07-04', '2024-07-04',
        # 劳动节（9月第一个周一）
        '2018-09-03', '2019-09-02', '2020-09-07', '2021-09-06', '2022-09-05', '2023-09-04', '2024-09-02',
        # 感恩节（11月第四个周四）
        '2018-11-22', '2019-11-28', '2020-11-26', '2021-11-25', '2022-11-24', '2023-11-23', '2024-11-28',
        # 圣诞节
        '2018-12-25', '2019-12-25', '2020-12-25', '2021-12-24', '2022-12-26', '2023-12-25', '2024-12-25',
    ]
    us_holidays = pd.to_datetime(us_holidays)
    trading_days = trading_days.difference(us_holidays)
    return trading_days